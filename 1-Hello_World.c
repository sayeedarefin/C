#include <stdio.h>

int main() {
  printf("Hello World!");
  printf("I am learning C.");
  printf("And it is awesome!\n");

  //To insert a new line, you can use the \n character:
    printf("Where there is a will,\n");
    printf("There is a way");

//Two \n characters after each other will create a blank line:
    printf("Where there is a will,\n\n");
    printf("There is a way");
  return 0;
}

//The newline character (\n) is called an escape sequence